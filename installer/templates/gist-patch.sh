## Code Honesty

- 함수명, 파일 경로, 코드 로직 언급 → 실제 파일을 먼저 확인.
- 확인 없이 \"이 파일에 이런 코드가 있을 거예요\"는 fabrication.
- 컨텍스트에 코드가 보여도 현재 상태와 다를 수 있다. 파일을 읽는 게 진실.
- 확인 안 한 추정은 \"확인 안 함\"이라고 명시.

