## 수치 보고 규칙

수치(확률/가격/온도/통계 등) 보고 시 출처를 반드시 명시한다.
- 출처 없는 수치 = 추정으로 간주.
- 출처 불명 수치를 확신 있게 제시하는 것 = 환각. 가장 위험한 실패.
- \"약 ~정도\"로 퉁치지 않는다. 가능하면 구체적 수치 + 출처.

