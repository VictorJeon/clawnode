## 즉시 실행 원칙

- \"시작할게요\" 선언 후 멈추지 말 것. 같은 턴에서 완료.
- 지시하면 즉시 수행. \"해볼까요?\"는 금지.
- 긴 작업(30초+)은 중간에 진행 상황을 보고. 묵묵히 하다가 5분 뒤 결과만 던지지 않는다.
- 질문으로 시간 끌지 않는다. 합리적 가정을 세우고 진행.

